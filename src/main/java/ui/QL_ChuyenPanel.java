    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
       StringBuilder sb = new StringBuilder();
        datavailditor.validataEmty(txtMatuyen, sb, "Mã tuyến không được để trống");
        datavailditor.validataEmty(txtGiodi, sb, "Giờ đi không được để trống");
        datavailditor.validataEmty(txtGiave, sb, "Giá vé không được để trống");
        datavailditor.validataEmty(txtNgaydi, sb, "Ngày đi không được để trống");
        if (sb.length()>0){
            messagedialoghelper.showErorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try{
           ChuyenXe cx = new ChuyenXe();        
           cx.setMaTuyen(Integer.parseInt(txtMatuyen.getText()));          
           cx.setGioDi(Time.valueOf(txtGiodi.getText()));
           cx.setGiaVe(Double.parseDouble(txtGiave.getText()));
           cx.setNgayDi(Date.valueOf((txtNgaydi.getText())));
           cx.setTrangThai("Còn vé"); 
           
           ChuyenXeDao dao = new ChuyenXeDao();
           if(dao.insert(cx)){
               messagedialoghelper.showMessageDialog(parentForm, "Thêm chuyến thành công", "Thông báo");
               loadDataToTable();
           }else{
               messagedialoghelper.showConfirmDialog(parentForm, "Chuyến không được lưu do lỗi", "Cảnh báo");
           }
       } catch (Exception e){
           e.printStackTrace();
           messagedialoghelper.showErorDialog((parentForm), e.getMessage(), "Lỗi");
       }
    }//GEN-LAST:event_btnThemActionPerformed

