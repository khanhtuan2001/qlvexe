    public void loadDataToTable(){
        try{
            ChuyenXeDao dao = new ChuyenXeDao();
            List<ChuyenXe> list = dao.findAll();
            tblModel.setRowCount(0);
            for(ChuyenXe it : list){
                tblModel.addRow(new Object[]{
                it.getMaChuyen(), it.getMaTuyen(), it.getNgayDi(), it.getGioDi(), it.getGiaVe()
            });
            }
            tblModel.fireTableDataChanged();
        } catch(Exception e)
        {
            e.printStackTrace();
            messagedialoghelper.showErorDialog(parentForm, e.getMessage(), "Loi");
        }
    }
    
     public void LoadTuyenToCombobox(){
        try{
        Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
        String connectionUrl = "jdbc:sqlserver://localhost;databaseName=QLVEXE;user=sa;password=123";
        Connection con = DriverManager.getConnection(connectionUrl);
        String sql ="select MaTuyen,TenTuyen from TuyenXe";
        Statement st =con.createStatement();
            PreparedStatement pstmt = con.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            cbxTuyen.removeAllItems();
            while (rs.next()){
                cbxTuyen.addItem(rs.getString("TenTuyen"));
                cbxTuyen.setSelectedItem("");
            }
            rs.close();
            pstmt.close();
            con.close();
        }catch(Exception e)
        {
            e.getStackTrace();
        }
    }      
