    private void tblChuyenXeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChuyenXeMouseClicked
        try{
            int row = tblChuyenXe.getSelectedRow();
            
            if(row >=0){
                int id = (int) tblChuyenXe.getValueAt(row,0);
                ChuyenXeDao dao = new ChuyenXeDao();
                ChuyenXe cx = dao.finBuyId(id);
                
                if( cx != null){
                    txtMachuyen.setText(String.valueOf(cx.getMaChuyen()));
                    txtMatuyen.setText(String.valueOf(cx.getMaTuyen()));
                    txtGiodi.setText(cx.getGioDi().toLocalTime().toString());
                    txtGiave.setText(cx.getGiaVe().toString());
                    txtNgaydi.setText(cx.getNgayDi().toString());
                }
            }
        } catch (Exception e){
            e.printStackTrace();
            messagedialoghelper.showErorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblChuyenXeMouseClicked

