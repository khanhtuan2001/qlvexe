    private void tblChuyenXeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblChuyenXeMouseClicked
           try{
            int row = tblChuyenXe.getSelectedRow();
            
            if(row >=0){
                int id = (int) tblChuyenXe.getValueAt(row,0);
                ChuyenXeDao dao = new ChuyenXeDao();
                ChuyenXe cx = dao.finBuyId(id);
                VeXeDao vexedao  = new VeXeDao();
                soluongghe = vexedao.DemSoGhe(cx.getMaChuyen());
                
                
                if( cx != null && soluongghe<=2){                
                    txtNgaydi.setText(cx.getNgayDi().toString());
                    txtGiodi.setText(cx.getGioDi().toString());
                    txtMaChuyen.setText(String.valueOf(cx.getMaChuyen()));                
                    curTime();
                    txtNhanVien.setText(String.valueOf(ShareData.nguoidangnhap.getManv())); 
                    txtThanhTien.setText(String.valueOf(cx.getGiaVe()));
                    soluongghe = vexedao.DemSoGhe(Integer.parseInt(txtMaChuyen.getText().toString()));
                    lblCountGhe.setText(String.valueOf(soluongghe));
                }else if(cx != null && soluongghe > 2){
                    messagedialoghelper.showErorDialog(parentForm, "Chuyến này đã hết vé", "Lỗi");
                }
            }
        } catch (Exception e){
            e.printStackTrace();
            messagedialoghelper.showErorDialog(parentForm, e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblChuyenXeMouseClicked

