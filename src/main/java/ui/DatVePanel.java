    private void txtSdtCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_txtSdtCaretUpdate
        KhachHangDao dao = new KhachHangDao();
        String a = txtSdt.getText();
        try {
            KhachHang kh = dao.findbySDT(a);
            if (kh != null)
            {
                txtHoten.setText(kh.getTenKH());
                txtNgaySinh.setText(kh.getNgaySinh().toLocalDate().toString());
                txtMaKh.setText(String.valueOf(kh.getMaKH()));
            }else{
               txtHoten.setText("");
               txtNgaySinh.setText("");
               txtMaKh.setText("");
            }
        } catch (Exception ex) {
            Logger.getLogger(DatVePanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtSdtCaretUpdate

