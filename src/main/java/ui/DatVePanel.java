    public void loadDataToTable(){     
        try{
//            tblModel = new DefaultTableModel();
            tblModel = (DefaultTableModel) tblChuyenXe.getModel();
            tblModel.setColumnIdentifiers(new String[]{"Mã chuyến", "Mã tuyến","Ngày đi", "Giờ đi", "Giá vé","Trạng Thái"});
            tblChuyenXe.setModel(tblModel);
            
            ChuyenXeDao dao = new ChuyenXeDao();
            List<ChuyenXe> list = dao.findAll();
            tblModel.setRowCount(0);
            
            for(ChuyenXe it : list){            
                tblModel.addRow(new Object[]{
                it.getMaChuyen(), it.getMaTuyen(), it.getNgayDi(), it.getGioDi(), it.getGiaVe(), it.getTrangThai()
            });
            }            
            tblModel.fireTableDataChanged();

        } catch(Exception e)
        {
            e.printStackTrace();
            messagedialoghelper.showErorDialog(parentForm, e.getMessage(), "Loi");
        }
    }
