    public void LoadTuyenToCombobox(){
        try{
        Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
        String connectionUrl = "jdbc:sqlserver://localhost;databaseName=QLVEXE;user=sa;password=123";
        Connection con = DriverManager.getConnection(connectionUrl);
        String sql ="select TenTuyen from TuyenXe";
        Statement st =con.createStatement();
            PreparedStatement pstmt = con.prepareStatement(sql);
            ResultSet rs = pstmt.executeQuery();
            cbxMaTuyen.removeAllItems();
            while (rs.next()){
                cbxMaTuyen.addItem(rs.getString("TenTuyen"));
                cbxMaTuyen.setSelectedItem("");
            }
            rs.close();
            pstmt.close();
            con.close();
        }catch(Exception e)
        {
            e.getStackTrace();
        }
     }
    
