           //ThemKH neu KH chua ton tai
           KhachHangDao khdao =new KhachHangDao();
           String a = txtSdt.getText();
            KhachHang kh = null;
            try {
                kh = khdao.findbySDT(a);
            } catch (Exception ex) {
                Logger.getLogger(DatVePanel.class.getName()).log(Level.SEVERE, null, ex);
            }
            if (kh != null)
            {
                VeXe vx = new VeXe();        
                vx.setMaChuyen(Integer.parseInt(txtMaChuyen.getText()));          
                vx.setMaKH((kh.getMaKH()));
                vx.setMaNV(Integer.parseInt(txtNhanVien.getText()));
                if(soluongghe == 0){
                   vx.setMaSoGhe("1"); 
                }else{
                    masoghe = String.valueOf((soluongghe+1));
                    vx.setMaSoGhe(masoghe);
                }              
                vx.setTongTien((int) Double.parseDouble(txtThanhTien.getText()));
                vx.setThoiGianDat(Date.valueOf((txtNgaydat.getText())));
                VeXeDao dao = new VeXeDao();               
               try {
                   if(dao.Insert(vx)){
                       messagedialoghelper.showMessageDialog(parentForm, "Thêm vé thành công", "Thông báo");
                       ChuyenXeDao chuyenXeDao = new ChuyenXeDao();
                       ChuyenXe cx = chuyenXeDao.finBuyId(vx.getMaChuyen());
                       if(dao.DemSoGhe(vx.getMaChuyen()) > 2){                          
                           cx.setTrangThai("Hết vé");
                           chuyenXeDao.UpdateChuyeXe(cx);
                       }else{
                            cx.setTrangThai("Còn vé");
                            chuyenXeDao.UpdateChuyeXe(cx);
                       }
                       ResetData();
                       loadDataToTable();
                   }else{
                       messagedialoghelper.showConfirmDialog(parentForm, "Vé không được lưu do lỗi", "Cảnh báo");
                   }
               } catch (Exception ex) {
                   Logger.getLogger(DatVePanel.class.getName()).log(Level.SEVERE, null, ex);
               }
            }else{
               //Them KH neu KH la KH moi
                KhachHang khdatve = new KhachHang();
                khdatve.setTenKH(txtHoten.getText());
                khdatve.setSdt(txtSdt.getText());
                khdatve.setNgaySinh(Date.valueOf((txtNgaySinh.getText())));            
               try {
                   khdao.insert(khdatve);
                   VeXe cx = new VeXe();        
                cx.setMaChuyen(Integer.parseInt(txtMaChuyen.getText()));          
                cx.setMaKH((khdatve.getMaKH()));
                cx.setMaNV(Integer.parseInt(txtNhanVien.getText()));
                cx.setMaSoGhe((txtMaGhe.getText()));
                cx.setTongTien((int) Double.parseDouble(txtThanhTien.getText()));
                cx.setThoiGianDat(Date.valueOf((txtNgaydat.getText())));
                
                VeXeDao dao = new VeXeDao();
               try {
                   if(dao.Insert(cx)){
                       messagedialoghelper.showMessageDialog(parentForm, "Thêm vé thành công", "Thông báo");
                       loadDataToTable();
                   }else{
                       messagedialoghelper.showConfirmDialog(parentForm, "Vé không được lưu do lỗi", "Cảnh báo");                               
                   }
               } catch (Exception ex) {
                   Logger.getLogger(DatVePanel.class.getName()).log(Level.SEVERE, null, ex);
               }
                
                
               } catch (Exception ex) {
                   Logger.getLogger(DatVePanel.class.getName()).log(Level.SEVERE, null, ex);
               }
    }//GEN-LAST:event_btnDatVeActionPerformed
